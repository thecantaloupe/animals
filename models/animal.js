//////////////////////////////////////////////
// Import Dependencies
//////////////////////////////////////////////
const mongoose = require("./connection")

////////////////////////////////////////////////
// Define Model
////////////////////////////////////////////////
// pull schema and model from mongoose
const { Schema, model } = mongoose;

// make animals schema
const animalsSchema = new Schema({
  name: String,
  color: String,
  readyToEat: Boolean,
  username: String,
});

// make animal model
const Animal = model("Animal", animalsSchema);

///////////////////////////////////////////////////
// Export Model
///////////////////////////////////////////////////
module.exports = Animal
